package cdc.gov.upload.client.model;

public class Metadata {

    public String filename;
    public String filetype;
    public String meta_destination_id;
    public String meta_ext_event;
    public String meta_ext_filename;
    public String meta_ext_objectkey;
    public String meta_ext_source;
    public String meta_ext_sourceversion;
    public String meta_file_timestamp;
    public String meta_username;
    public String original_file_timestamp;
    
    public String getFilename() {
        return filename;
    }
    public void setFilename(String filename) {
        this.filename = filename;
    }
    public String getFiletype() {
        return filetype;
    }
    public void setFiletype(String filetype) {
        this.filetype = filetype;
    }
    public String getMeta_destination_id() {
        return meta_destination_id;
    }
    public void setMeta_destination_id(String meta_destination_id) {
        this.meta_destination_id = meta_destination_id;
    }
    public String getMeta_ext_event() {
        return meta_ext_event;
    }
    public void setMeta_ext_event(String meta_ext_event) {
        this.meta_ext_event = meta_ext_event;
    }
    public String getMeta_ext_filename() {
        return meta_ext_filename;
    }
    public void setMeta_ext_filename(String meta_ext_filename) {
        this.meta_ext_filename = meta_ext_filename;
    }
    public String getMeta_ext_objectkey() {
        return meta_ext_objectkey;
    }
    public void setMeta_ext_objectkey(String meta_ext_objectkey) {
        this.meta_ext_objectkey = meta_ext_objectkey;
    }
    public String getMeta_ext_source() {
        return meta_ext_source;
    }
    public void setMeta_ext_source(String meta_ext_source) {
        this.meta_ext_source = meta_ext_source;
    }
    public String getMeta_ext_sourceversion() {
        return meta_ext_sourceversion;
    }
    public void setMeta_ext_sourceversion(String meta_ext_sourceversion) {
        this.meta_ext_sourceversion = meta_ext_sourceversion;
    }
    public String getMeta_file_timestamp() {
        return meta_file_timestamp;
    }
    public void setMeta_file_timestamp(String meta_file_timestamp) {
        this.meta_file_timestamp = meta_file_timestamp;
    }
    public String getMeta_username() {
        return meta_username;
    }
    public void setMeta_username(String meta_username) {
        this.meta_username = meta_username;
    }
    public String getOriginal_file_timestamp() {
        return original_file_timestamp;
    }
    public void setOriginal_file_timestamp(String original_file_timestamp) {
        this.original_file_timestamp = original_file_timestamp;
    }
}
