package cdc.gov.upload.client.model;

public class ExtEvent {

    public String name;
    public String definition_filename;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getDefinition_filename() {
        return definition_filename;
    }
    public void setDefinition_filename(String definition_filename) {
        this.definition_filename = definition_filename;
    }
}
